class House(self, name, number_of_floors):
    def __init__(self):
        self.name = name
        self.number_of_floors = number_of_floors
        
    def go_to(new_floor):
        if new_floor > self.number_of_floors:
            print(f"Этажа {new_floor} не существует")
        else:
            for floor in new_floor:
                print(floor)
              
